    private void jbtnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtnAgregarMouseClicked
        // TODO add your handling code here:
        Proveedor pr = new Proveedor();
        //pr.setIdProveedor(Integer.parseInt(this.jtxtCodigo.getText()));
        pr.setNombre(this.jtxtProveedor.getText());
        pr.setRegistro(this.jtxtRegistro.getText());
        pr.setDireccion(this.jtxtDireccion.getText());
        pr.setTelefono(this.jtxtTelefono.getText());
        pr.setCelular(this.jtxtCelular.getText());
        pr.setEmail(this.jtxtEmail.getText());
        pr.setContacto(this.jtxtContacto.getText());
        pr.setActivo(this.jchkFiltroActivo.isSelected());
        controller.agregarProveedor(pr);
        controller = new ControllerProveedor();
        this.jtblProveedores.setModel(controller);
    }//GEN-LAST:event_jbtnAgregarMouseClicked

