    private void jtblProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblProveedoresMouseClicked
        // TODO add your handling code here:
        int x = 0;
        x = this.jtblProveedores.getSelectedRow();
        Proveedor pr = (Proveedor) controller.getListaProveedores().get(x);
        this.jtxtCodigo.setText(Integer.toString(pr.getIdProveedor()));
        this.jtxtProveedor.setText(pr.getNombre());
        this.jtxtRegistro.setText(pr.getRegistro());
        this.jtxtDireccion.setText(pr.getDireccion());
        this.jtxtTelefono.setText(pr.getTelefono());
        this.jtxtCelular.setText(pr.getCelular());
        this.jtxtEmail.setText(pr.getEmail());
        this.jtxtContacto.setText(pr.getContacto());
        this.jchkFiltroActivo.setSelected(pr.getActivo());
    }//GEN-LAST:event_jtblProveedoresMouseClicked

    private void jbtnFiltrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtnFiltrarMouseClicked
        // TODO add your handling code here:
        boolean filtro1 = this.jchkFiltroNombres.isSelected();
        boolean filtro2 = this.jchkFiltroApellido1.isSelected();
        controller = new ControllerProveedor();
        Filtro filtro = new Filtro(filtro1,filtro2,this.jtxtProveedor.getText(),this.jtxtRegistro.getText());
        controller.setFiltro(filtro);
        controller.getRows();
        this.jtblProveedores.setModel(controller);
    }//GEN-LAST:event_jbtnFiltrarMouseClicked

